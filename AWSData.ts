// ─── Internal helpers ────────────────────────────────────────────────────────

const FHMZ_URL = "https://www.fhmzbih.gov.ba/latinica/AKTUELNO/Automatske.php";
const CORS_PROXY = "https://cors.eu.org/?";

/** Parse a cell string to float, returning null for empty / error codes. */
function parseNum(raw: string): number | null {
  const s = raw.trim().replace(",", ".");
  if (!s || s === "" || s === "-") return null;
  const v = parseFloat(s);
  if (isNaN(v)) return null;
  // 999 is a known sensor error code used on this site
  if (v === 999) return null;
  return v;
}

/** Return null for blank / wildcard cells. */
function parseStr(raw: string): string | null {
  const s = raw.trim();
  return s === "" || s === "" || s === "-" ? null : s;
}

/** Pull text from all td children of a tr. */
function cells(tr: Element): string[] {
  return Array.from(tr.querySelectorAll("td")).map((td) => td.textContent?.trim() ?? "");
}

/** All data rows from a table (skips header rows that have no text). */
function dataRows(table: Element | undefined): Element[] {
  if (!table) return [];
  return Array.from(table.querySelectorAll("tr")).filter((tr) => tr.querySelectorAll("td").length > 1);
}

// ─── Main export ─────────────────────────────────────────────────────────────

export async function scrape(): Promise<ScrapedData> {
  const useProxy = typeof process === "undefined" || !process.env?.FHMZ_NO_PROXY;
  const url = useProxy ? CORS_PROXY + encodeURIComponent(FHMZ_URL) : FHMZ_URL;

  try {
    const res = await fetch(url);
    if (!res.ok) throw new Error(`FHMZ fetch failed: HTTP ${res.status}`);

    const html = await res.text();
    const doc = new DOMParser().parseFromString(html, "text/html");
    const tables = Array.from(doc.querySelectorAll("table"));

    if (tables.length < 3) {
      console.warn(`Expected 3+ tables, got ${tables.length}. Falling back to dummy data.`);
      return fhmzRealDummyData as ScrapedData;
    }

    // Report date printed on page
    const bodyText = doc.body?.innerText ?? "";
    const dateMatch = bodyText.match(/datum[:\s]*(\d{1,2}[.-/]\d{1,2}[.-/]\d{4})/i);
    const reportDate = dateMatch ? dateMatch[1] : null;

    const precipitation: PrecipitationStation[] = dataRows(tables[0]).map((tr) => {
      const c = cells(tr);
      return {
        type: "precipitation",
        time: c[0] ?? "",
        city: c[1] ?? "",
        tempC: parseNum(c[2] ?? ""),
        humidityPct: parseNum(c[3] ?? ""),
        precipMm: parseNum(c[4] ?? ""),
      };
    });

    const agro: AgroStation[] = dataRows(tables[1]).map((tr) => {
      const c = cells(tr);
      return {
        type: "agro",
        time: c[0] ?? "",
        station: c[1] ?? "",
        windDir: parseStr(c[2] ?? ""),
        windSpeedMs: parseNum(c[3] ?? ""),
        pressureHpa: parseNum(c[4] ?? ""),
        tempC: parseNum(c[5] ?? ""),
        humidityPct: parseNum(c[6] ?? ""),
        precipMm: parseNum(c[7] ?? ""),
      };
    });

    const meteo: MeteoStation[] = dataRows(tables[2]).map((tr) => {
      const c = cells(tr);
      return {
        type: "meteo",
        date: c[0] ?? "",
        time: c[1] ?? "",
        station: c[2] ?? "",
        windDir: parseStr(c[3] ?? ""),
        windSpeedMs: parseNum(c[4] ?? ""),
        pressureHpa: parseNum(c[5] ?? ""),
        tempC: parseNum(c[6] ?? ""),
        humidityPct: parseNum(c[7] ?? ""),
        precipMm: parseNum(c[8] ?? ""),
      };
    });

    const airQuality: AirQualityStation[] = dataRows(tables[3] || []).map((tr) => {
      const c = cells(tr);
      return {
        type: "air_quality",
        date: c[0] ?? "",
        time: c[1] ?? "",
        station: c[2] ?? "",
        windDir: parseStr(c[3] ?? ""),
        windSpeedMs: parseNum(c[4] ?? ""),
        windSpeedKmh: parseNum(c[5] ?? ""),
        pressureHpa: parseNum(c[6] ?? ""),
        tempC: parseNum(c[7] ?? ""),
        humidityPct: parseNum(c[8] ?? ""),
      };
    });

    const total = precipitation.length + agro.length + meteo.length + airQuality.length;
    console.log(`✓ Successfully scraped live data: ${total} stations`);
    console.log(`  - Precipitation: ${precipitation.length}`);
    console.log(`  - Agro: ${agro.length}`);
    console.log(`  - Meteo: ${meteo.length}`);
    console.log(`  - Air Quality: ${airQuality.length}`);
    console.log("Meteo stations:", meteo.map(m => `${m.station} (${m.tempC}°C)`));
    return {
      scrapedAt: new Date().toISOString(),
      reportDate,
      precipitation,
      agro,
      meteo,
      airQuality,
      all: [...precipitation, ...agro, ...meteo, ...airQuality],
    };
  } catch (error) {
    console.error("Failed to scrape live data, falling back to dummy:", error);
    return fhmzRealDummyData as ScrapedData;
  }
}
